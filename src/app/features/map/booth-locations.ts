export function normalizeBoothLabel(value: string): string {
  return value
    .trim()
    .toUpperCase()
    .replace(/^BOOTH\s*/, '')
    .replace(/^#\s*/, '')
    .replace(/\s+/g, '');
}

export function findBoothLocation(value: string): BoothLocation | null {
  const number = normalizeBoothLabel(value);
  const point = boothLocations[number];

  return point ? { number, xPercent: point[0], yPercent: point[1] } : null;
}
